export const getDepartamentos = () => Object.keys(PERU_UBIGEO).sort();

export const getProvincias = (departamento) => {
  if (!departamento) return [];
  return Object.keys(PERU_UBIGEO[departamento] || {}).sort();
};

export const getDistritos = (departamento, provincia) => {
  if (!departamento || !provincia) return [];
  return (PERU_UBIGEO[departamento]?.[provincia] || []).sort();
};
